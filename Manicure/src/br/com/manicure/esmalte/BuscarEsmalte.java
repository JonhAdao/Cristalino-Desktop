    private void searchEsmalte() {
        String busca = this.tBuscarEsmalte.getText();
        List<Esmalte> lista;
        lista = DAOFactory.getEsmalteDAO().filtrar(busca);
        if (lista != null) {
            this.tableModelEsmalte.addLista(lista);
        }
        this.tBuscarEsmalte.requestFocusInWindow();

    }
