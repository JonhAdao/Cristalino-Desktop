    private void bOkEsmalteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bOkEsmalteMouseClicked
        int row = this.tableEsmalte.getSelectedRow();
        if (row > -1) {
            Esmalte e = this.tableModelEsmalte.getEsmalte(row); // variavel p recebe pacote da lista que foi selecionado
            this.agenda.setCorEsmalte(e);

            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um esmalte da tabela", "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bOkEsmalteMouseClicked

