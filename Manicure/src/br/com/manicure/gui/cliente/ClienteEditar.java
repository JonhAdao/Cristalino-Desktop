    private void searchCliente() {
        String busca = this.tBuscarCliente.getText();
        List<Cliente> lista;
        lista = DAOFactory.getClienteDAO().filtrar(busca);
        if (lista != null) {
            this.tableModelClientes.addLista(lista);
        }
        this.tBuscarCliente.requestFocusInWindow();

    }

