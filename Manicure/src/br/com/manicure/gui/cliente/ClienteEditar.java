    private void bOkClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bOkClienteMouseClicked
        int row = this.tableClientes.getSelectedRow();
        if (row > -1) {
            Cliente c = this.tableModelClientes.getCliente(row);
            this.agenda.setCliente(c);

            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um(a) cliente da tabela", "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bOkClienteMouseClicked

